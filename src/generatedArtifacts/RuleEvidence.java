package generatedArtifacts;

public class RuleEvidence {
	String mName;
	Evidences tempProperty;
	
	public RuleEvidence(String mName, Evidences tempProperty) {
		super();
		this.mName = mName;
		this.tempProperty = tempProperty;
	}
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public Evidences getTempProperty() {
		return tempProperty;
	}
	public void setTempProperty(Evidences tempProperty) {
		this.tempProperty = tempProperty;
	}
	

}
