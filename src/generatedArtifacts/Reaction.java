package generatedArtifacts;

public class Reaction {
	String agent1;
	String agent;
	public Reaction(String agent1, String agent) {
		super();
		this.agent1 = agent1;
		this.agent = agent;
	}
	public Reaction(String agent1) {
		super();
		this.agent1 = agent1;
	}
	public String getAgent1() {
		return agent1;
	}
	public void setAgent1(String agent1) {
		this.agent1 = agent1;
	}
	public String getAgent() {
		return agent;
	}
	public void setAgent(String agent2) {
		this.agent = agent2;
	}
	

}
